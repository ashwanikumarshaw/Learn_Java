package oops;

public class About_OOPs {
	/*
	 * OOP stands for Object-Oriented Programming.

	Procedural programming is about writing procedures or 
	methods that perform operations on the data, 
	while object-oriented programming is about creating objects that contain both data and methods.

	Object-oriented programming has several advantages over procedural programming:

		OOP is faster and easier to execute
		OOP provides a clear structure for the programs
		OOP helps to keep the Java code DRY "Don't Repeat Yourself", and makes the code easier to maintain, modify and debug
		OOP makes it possible to create full reusable applications with less code and shorter development time
		
		Inheritance
		When one object acquires all the properties and behaviors of a parent object, 
		it is known as inheritance. It provides code reusability. 
		It is used to achieve runtime polymorphism.
		
		Polymorphism
		If one task is performed in different ways, it is known as polymorphism.
		In Java, we use method overloading and method overriding to achieve polymorphism.
		
		Abstraction
		Hiding internal details and showing functionality is known as abstraction	
		In Java, we use abstract class and interface to achieve abstraction
		
		Encapsulation
		Binding (or wrapping) code and data together into a single unit are known as encapsulation. 

	 */

}
