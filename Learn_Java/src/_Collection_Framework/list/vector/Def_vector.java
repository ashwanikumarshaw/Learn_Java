package _Collection_Framework.list.vector;

import java.util.Vector;

public class Def_vector {

	public static void main(String[] args) {
		/*
		 * Vector implements a dynamic array that means 
		 * it can grow or shrink as required. Like an array, 
		 * it contains components that can be accessed using an 
		 * integer index.
		 * 
		 * They are very similar to ArrayList but Vector 
		 * is synchronised and have some legacy method 
		 * which collection framework does not contain.
		 * 
		 */

		 Vector<String> v = new Vector<>(); 
		 v.add("ASA");
		 
	}

}
